#include<iostream>
using namespace std;

int main()
{                  
    int a=400,b=200;                                  //arithmetic operators in c++

cout<<"The value of a+b is"<<a+b<<endl;
cout<<"The value of a-b is"<<a-b<<endl;
cout<<"The value of a*b is"<<a*b<<endl;
cout<<"The value of a/b is"<<a/b<<endl;
cout<<"The value of a++ is"<<a++<<endl;
cout<<"The value of a-- is"<<a--<<endl;
cout<<"The value of ++a is"<<++a<<endl;
cout<<"The value of --a is"<<--a<<endl;


return 0;
}